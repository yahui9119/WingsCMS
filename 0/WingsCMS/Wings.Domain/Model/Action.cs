using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Wings.Domain.Model
{
    /// <summary>
    /// 标示可访问点的领域的实体
    /// </summary>
    public class Action : AggregateRoot
    {
        public Action()
        {
            ChildAction = new List<Action>();
        }
        public virtual Web web { get; set; }
        /// <summary>
        /// 模块控制器
        /// </summary>
        public virtual  string Controller { get; set; }
        /// <summary>
        /// 模块访问点
        /// </summary>
        public virtual string ActionName { get; set; }
        /// <summary>
        /// 说明 简介
        /// </summary>
        public virtual string Description { get; set; }
        ///// <summary>
        ///// 父访问点
        ///// </summary>
        public virtual Action ParentAction { get; set; }
        /// <summary>
        /// 子访问点
        /// </summary>
        public virtual  List<Action> ChildAction { get; set; }

        /// <summary>
        /// 是否是按钮
        /// </summary>
        public virtual bool IsButton { get; set; }
    }
}
